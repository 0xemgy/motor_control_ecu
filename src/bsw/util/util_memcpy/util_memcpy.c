void util_memcpy(void *dst, const void *src, uint32_t len)
{
    uint8_t       *dst0 = dst;
    const uint8_t *src0 = src;

    for (uint32_t i = (uint32_t)0; i < len; i++)
    {
        dst0[i] = src0[i];
    }
}
